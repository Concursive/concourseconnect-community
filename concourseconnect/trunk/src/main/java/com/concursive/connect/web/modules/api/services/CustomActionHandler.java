package com.concursive.connect.web.modules.api.services;

import com.concursive.connect.web.modules.api.beans.TransactionItem;

import java.sql.Connection;
import java.sql.SQLException;

/**
 * An interface for processing custom sync api actions; A CUSTOM_ACTION is when
 * an object has an action other than INSERT/UPDATE/DELETE/SELECT;
 * for example: <projectList action="backup" />
 *
 * @author matt rajkowski
 * @created March 13, 2008
 */
public interface CustomActionHandler {

  /**
   * The sync API will execute the process method when a custom action is
   * mapped to a CustomActionHandler class.  Additional methods can be created
   * and they will be seen by the API (for example action="status").
   *
   * @param transactionItem The transaction item container
   * @param db              A database connection for the action to use
   * @return Boolean success
   * @throws SQLException Description of the Exception
   */
  public boolean process(TransactionItem transactionItem, Connection db) throws Exception;

}
